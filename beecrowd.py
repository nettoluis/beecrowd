'''Solicitar qual o salário atual do funcionário e retornar qual o novo salário, salarioAtual variação absoluta e salarioAtual variação percentual'''
salarioAtual = float(input())
if salarioAtual <= 400:
    percentualDeAumento = 115
elif salarioAtual > 400 and salarioAtual <= 800:
    percentualDeAumento = 112
elif salarioAtual > 800 and salarioAtual <= 1200:
    percentualDeAumento = 110
elif salarioAtual > 1200 and salarioAtual <= 2000:
    percentualDeAumento = 107
else:
    percentualDeAumento = 104
novoSalario = (salarioAtual * percentualDeAumento)/100
reajusteGannho = novoSalario - salarioAtual
emPercentual = percentualDeAumento - 100
print(f'Novo salario: {novoSalario:.2f}\nReajuste ganho: {reajusteGannho:.2f}\nEm percentual: {emPercentual} %')